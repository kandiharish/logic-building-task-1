"""**Problem 5: Calculate Attendance Percentage**

**Problem Statement**: Calculate the attendance percentage from a list containing "P" (Present) and "A" (Absent).
"""

# Attendance list  containing P and A , "P" (Present) and "A" (Absent)
attendance = ["P", "P", "A", "P", "P"]
present_count = 0
# Count total  number of P
for day in attendance:
    if day == "P":
        present_count += 1
# Calculate percentage of attendance
total_days = len(attendance)
percentage = (present_count / total_days) * 100
# Display result:
print("Attendance Percentage:", percentage)

