"""**Problem 7: Count Character Frequency**

**Problem Statement** : Count how many times each character appears in a given string.
"""

# Input string
text = "python"
# intialize an Empty dictionary to store character and its count
freq = {}
# Count characters by traversing each character in a string
for ch in text:
    if ch in freq:
        freq[ch] += 1
    else:
        freq[ch] = 1
# Display result:
print(freq)

