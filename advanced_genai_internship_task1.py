"""**5. Error Message Detector**

**Problem Statement:** Detect error messages from system logs. logs = ["INFO", "ERROR", "WARNING", "ERROR"]

**Requirements:** Count the number of "ERROR" entries Print the total error count Real-World Application: Monitoring and log analysis systems
"""

## System logs
logs = ["INFO", "ERROR", "WARNING", "error"]

#Count the number of "ERROR" entries in the logs list, ignoring case sensitivity
error_count = sum(1 for log in logs if log.upper() == "ERROR")

#Printing the total error count
print("The total ERROR count:", error_count)