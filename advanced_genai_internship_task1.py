"""**2. Pass / Fail Analyzer**

**Problem Statement:** Analyze student results. marks = [45, 78, 90, 33, 60]

**Requirements:** A student passes if marks ≥ 50 Count the total number of pass students Count the total number of fail students Print the final result clearly Real-World Application: Academic evaluation systems
"""

#input a list of marks
marks = [45, 78, 90, 33, 60]

#count the total number of pass and fail students
total_pass = sum(1 for mark in marks if mark >= 50)
total_fail = sum(1 for mark in marks if mark < 50)

#Printing the final result
print(f"Count the total number of pass students : {total_pass}")
print(f"Count the total number of fail students : {total_fail}")

"""**3. Simple Data Cleaner**

**Problem Statement:**  Clean and standardize user names. names = [" Alice ", "bob", " CHARLIE "]

**Requirements:** Remove extra spaces Convert all names to lowercase Print the cleaned list Real-World Application: Data preprocessing before analysis
"""

#input a list of names
names = [" Alice ", "bob", " CHARLIE "]

## Create an empty list to store cleaned names
cleaned_list=[]

# Loop through each name in the original list
for name in names:

    # Remove leading and trailing spaces using strip()
    # Convert the name to lowercase using lower()
    cleaned_usernames =name.strip().lower()

    # Add the cleaned name to the new list
    cleaned_list.append(cleaned_usernames)

# Print the final cleaned list
print(cleaned_list)

